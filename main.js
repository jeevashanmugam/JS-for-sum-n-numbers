
let a = 10;
sum = 0;
count = 0;
for (i = 1; i <= a; i++) {
    sum = sum + i
    count++
}

document.write("Sum of the numbers is : " + sum + "<br>")
document.write("Count of the numbers is : " + count + "<br>")
document.write("Avgerage of the (sum) numbers is : " + sum / count + "<br>")












